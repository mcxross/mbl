package xyz.mcxross.mbl.model;

enum class Ability(val bit: UByte) {
    Copy(1u),
    Drop(2u),
    Store(4u),
    Key(8u);

    fun requiredBy(): Ability {
        return if (this == Key) Store else this
    }
}